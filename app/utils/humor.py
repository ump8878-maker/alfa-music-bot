# Комментарии к позиции чата в рейтинге (#N из M)
CHAT_POSITION_COMMENTS = [
    "ваш чат слушает странную, но очень интересную смесь музыки",
    "здесь каждый со своим вкусом — и это круто",
    "от попсы до дарк-техно за один скролл",
    "музыкальный винегрет в лучшем смысле",
    "редкая смесь — таким чатом можно гордиться",
]


def get_rating_comment() -> str:
    """Случайный комментарий к месту в рейтинге участника."""
    return random.choice(RATING_COMMENTS)


def get_chat_rating_comment() -> str:
    """Случайный комментарий к рейтингу чата."""
    return random.choice(CHAT_RATING_COMMENTS)


def get_strange_taste_comment() -> str:
    """Текст для вирусного сообщения «странный вкус»."""
    return random.choice(STRANGE_TASTE_COMMENTS)


def get_chat_position_comment() -> str:
    """Комментарий к позиции чата (#N из M)."""
    return random.choice(CHAT_POSITION_COMMENTS)


def get_top_comment(position: int) -> str:
    """Комментарий к месту в топе (1, 2, 3...)."""
    comments_for_first = [
        "сегодня главный диджей чата",
        "вкус 100 из 100",
        "все бы так слушали",
    ]
    if position == 1:
        return random.choice(comments_for_first)
    return get_rating_comment()
