"""Короткие комментарии бота: скан чата, рост рейтинга. Без токсичности."""
import random
from typing import Optional, Any

CHAT_SCAN_COMMENTS = [
    "в этом чате явно не любят тишину",
    "тут есть люди, которые живут после полуночи",
    "слишком много техно для спокойной жизни",
    "кто-то тут точно мечтал стать диджеем",
    "смесь странная, но бодрая",
    "хороший вкус, но без внутренней драмы не обошлось",
    "здесь знают толк в сетах",
    "музыкально крепкий состав",
    "вайб сильный, статистика не врёт",
]

GROWTH_COMMENTS = [
    "у вас сильный состав, но не хватает пары музыкальных фанатиков",
    "ещё пара человек — и соседи по рейтингу позади",
    "музыкально крепко, статистически пока не добили",
    "ваш чат уже в игре, но можно затащить выше",
    "сильный состав, но топ ещё впереди",
    "ещё 2 человека и вы обгоните соседей по вкусу",
]


def get_chat_scan_comment(chat_profile: Optional[Any] = None) -> str:
    return random.choice(CHAT_SCAN_COMMENTS)


def get_growth_comment() -> str:
    return random.choice(GROWTH_COMMENTS)


def get_top_comment(position: int) -> str:
    """Комментарий к месту в рейтинге чата (1, 2, 3...)."""
    if position == 1:
        return random.choice([
            "сегодня главный диджей чата",
            "вкус на высоте",
            "все бы так слушали",
        ])
    return random.choice([
        "сегодня главный диджей чата",
        "в этом чате явно есть диджей",
        "у этого человека идеальный плейлист",
    ])
